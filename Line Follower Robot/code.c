void setup(){ 
pinMode(R_S, INPUT); 
pinMode(L_S, INPUT); 
pinMode(enA, OUTPUT); 
pinMode(RightBackward, OUTPUT); 
pinMode(RightForward, OUTPUT); 
pinMode(LeftForward, OUTPUT); 
pinMode(LeftBackward, OUTPUT); 
pinMode(enB, OUTPUT);
digitalWrite(enA, HIGH); 
digitalWrite(enB, HIGH); 
Serial.begin(9600);
// SSD1306_SWITCHCAPVCC = generate display voltage from 3.3V internally
  if(!display.begin(SSD1306_SWITCHCAPVCC, SCREEN_ADDRESS)) {
    Serial.println(F("SSD1306 allocation failed"));
    for(;;); // Don't proceed, loop forever
  }
  delay(2000); // Pause for 2 seconds
 
  // Clear the buffer.
  display.clearDisplay();
  
  // Draw bitmap on the screen
  display.drawBitmap(0, 0, image_data_20190410_114707, 128, 64, 1);
  display.display();
  delay(2000);
  display.clearDisplay();
  display.setTextColor(WHITE);
}
void loop(){  
  display.setCursor(10,1);
  display.setTextSize(2);
  display.print("CORECHAMP");
  delay(100);
  Serial.print(digitalRead(R_S));
  Serial.println(digitalRead(L_S));
  Serial.println(" ");
if((digitalRead(R_S) == 0)&&(digitalRead(L_S) == 0)){forward();}   //if Right Sensor and Left Sensor are at White color then it will call forword function
if((digitalRead(R_S) == 1)&&(digitalRead(L_S) == 0)){turnRight();} //if Right Sensor is Black and Left Sensor is White then it will call turn Right function  
if((digitalRead(R_S) == 0)&&(digitalRead(L_S) == 1)){turnLeft();}  //if Right Sensor is White and Left Sensor is Black then it will call turn Left function
if((digitalRead(R_S) == 1)&&(digitalRead(L_S) == 1)){Stop();} //if Right Sensor and Left Sensor are at Black color then it will call Stop function
delay(500);}
void forward(){  //forword
digitalWrite(RightBackward, LOW); //Right Motor forword Pin 
digitalWrite(RightForward, HIGH);  //Right Motor backword Pin 
digitalWrite(LeftForward, HIGH);  //Left Motor backword Pin 
digitalWrite(LeftBackward, LOW); //Left Motor forword Pin 
    display.clearDisplay();
    display.setCursor(10,1);
    display.setTextSize(2);
    display.print("CORECHAMP");
    display.setCursor(040,25);
    display.setTextSize(2);
    display.print("GOING");
    display.setCursor(20,50);
    display.setTextSize(2);
    display.print("FORWARD");
    display.display();
}
void turnRight(){ //turnRight
digitalWrite(RightBackward, HIGH);  //Right Motor forword Pin 
digitalWrite(RightForward, LOW); //Right Motor backword Pin  
digitalWrite(LeftForward, HIGH);  //Left Motor backword Pin 
digitalWrite(LeftBackward, LOW); //Left Motor forword Pin 
    display.clearDisplay();
    display.setCursor(10,1);
    display.setTextSize(2);
    display.print("CORECHAMP");
    display.setCursor(40,25);
    display.setTextSize(2);
    display.print("TURN");
    display.setCursor(40,50);
    display.setTextSize(2);
    display.print("RIGHT"); 
    display.display();
}
void turnLeft(){ //turnLeft
digitalWrite(RightBackward, LOW); //Right Motor forword Pin 
digitalWrite(RightForward, HIGH);  //Right Motor backword Pin 
digitalWrite(LeftForward, LOW); //Left Motor backword Pin 
digitalWrite(LeftBackward, HIGH);  //Left Motor forword Pin 
    display.clearDisplay();
    display.setCursor(10,1);
    display.setTextSize(2);
    display.print("CORECHAMP");
    display.setCursor(40,25);
    display.setTextSize(2);
    display.print("TURN");
    display.setCursor(40,50);
    display.setTextSize(2);
    display.print("LEFT");
    display.display(); 
}
void Stop(){ //stop
digitalWrite(RightBackward, LOW); //Right Motor forword Pin 
digitalWrite(RightForward, LOW); //Right Motor backword Pin 
digitalWrite(LeftForward, LOW); //Left Motor backword Pin 
digitalWrite(LeftBackward, LOW); //Left Motor forword Pin 
    display.clearDisplay();
    display.setCursor(10,1);
    display.setTextSize(2);
    display.print("CORECHAMP");
    display.setCursor(40,40);
    display.setTextSize(2);
    display.print("STOP");
    display.display();
}
