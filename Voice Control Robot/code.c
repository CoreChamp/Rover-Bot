void setup() {
  pinMode(enA,OUTPUT);
  pinMode(enB,OUTPUT);
  pinMode(revleft, OUTPUT); // Set Motor Pins As O/P
  pinMode(fwdleft, OUTPUT);
  pinMode(revright, OUTPUT);
  pinMode(fwdright, OUTPUT);
  Serial.begin(9600);
  Serial.println("ready");
  Bluetooth.begin(9600);
  
  // SSD1306_SWITCHCAPVCC = generate display voltage from 3.3V internally
  if(!display.begin(SSD1306_SWITCHCAPVCC, SCREEN_ADDRESS)) {
    Serial.println(F("SSD1306 allocation failed"));
    for(;;); // Don't proceed, loop forever
  }
  delay(2000); // Pause for 2 seconds
 
  // Clear the buffer.
  display.clearDisplay();
  
  // Draw bitmap on the screen
  display.drawBitmap(0, 0, image_data_20190410_114707, 128, 64, 1);
  display.display();
  delay(2000);
  display.clearDisplay();
  display.setTextColor(WHITE);
 
}
 
void loop() {
  
  display.setCursor(10,1);
  display.setTextSize(2);
  display.print("CORECHAMP");
  delay(100);
  display.display();
  delay(100);
  while(Bluetooth.available()) {
  command = "";  
  command = Bluetooth.readString();
  
    Serial.print(command);
}
  if(command == "*move forward#"){
    forward();
  }else if(command == "*move backward#"){
    backward();
  }else if(command == "*turn left#"){
    left();
  }else if(command == "*turn right#"){
    right();
  }else if(command == "*stop#") {
    Stop();
 }
  command = "";
}
 
void forward(){            //move forward(all motors rotate in forward direction)
    
    analogWrite(enA,200);
    analogWrite(enB,200);
    digitalWrite(fwdright, HIGH); // Move Forward
    digitalWrite(revright, LOW);
    digitalWrite(fwdleft, HIGH);                                
    digitalWrite(revleft, LOW); 
    display.clearDisplay();
    display.setCursor(10,1);
    display.setTextSize(2);
    display.print("CORECHAMP");
    display.setCursor(040,25);
    display.setTextSize(2);
    display.print("GOING");
    display.setCursor(20,50);
    display.setTextSize(2);
    display.print("FORWARD"); 
    delay(100);
}
 
void backward(){      //move reverse (all motors rotate in reverse direction)
    analogWrite(enA,200);
    analogWrite(enB,200);
    digitalWrite(fwdright, LOW); // Move Backward             
    digitalWrite(revright, HIGH);
    digitalWrite(fwdleft, LOW);                                
    digitalWrite(revleft, HIGH);
    display.clearDisplay();
    display.setCursor(10,1);
    display.setTextSize(2);
    display.print("CORECHAMP");
    display.setCursor(040,25);
    display.setTextSize(2);
    display.print("GOING");
    display.setCursor(15,50);
    display.setTextSize(2);
    display.print("BACKWARD"); 
    delay(100);
}
 
